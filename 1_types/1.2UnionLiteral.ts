// UNION TYPE : a variable can be given more than one primitive type.
// UNION TYPE : bir değişkene birden fazla ilkel tip verilebilir.

let myUnion: string | number | boolean = true;

console.log(myUnion);

// LITERAL TYPE : sets one or more variable values to the variable.
// LITERAL TYPE : değişkene bir veya daha fazla değişken değeri atar.
let myLiteral: "test1" | 2 | true = 2;

console.log(myLiteral);
